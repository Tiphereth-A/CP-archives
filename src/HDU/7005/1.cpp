using namespace std;
void solve(int t_ = 0) {
  int n, k;
  cin >> n >> k;
  vector<int> a(n);
  for (int &i : a) cin >> i;
  sort(a.begin(), a.end());
  int ans = 0, lst = INT_MIN;
  for (int i : a) {
    int l = i - k, r = i + k;
    if (lst >= r) continue;
    lst = max(lst + 1, l);
    ++ans;
  }
  cout << ans << '\n';
}
signed main() {
  std::ios::sync_with_stdio(false);
  std::cin.tie(nullptr);
  int i_ = 0;
  int t_ = 0;
  std::cin >> t_;
  for (i_ = 0; i_ < t_; ++i_) solve(i_);
  return 0;
}
